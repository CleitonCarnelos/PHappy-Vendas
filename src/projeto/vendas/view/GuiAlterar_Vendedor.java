    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o código do Vendedor desejado!");
            txtCodigo.requestFocus();
        } else {
            vendedor = null;
            vendedor = daoVendedor.consultarTbVend(parseInt(txtCodigo.getText()));

            if (vendedor == null) {
                JOptionPane.showMessageDialog(null, "Código inválido!");
                txtCodigo.requestFocus();
            } else {
                if (vendedor.getAtivo().equals("I")) {
                    JOptionPane.showMessageDialog(null, "Este vendedor esta desativado!"
                            + "\n" + "Insira um código válido!");

                    limpaCampos();
                } else {
                    txtCodigo.setText(String.valueOf(vendedor.getCodigo()));
                    txtNome.setText(vendedor.getNome());
                    ftxtRG.setText(vendedor.getRg());
                    ftxtTel.setText(vendedor.getTel());
                    txtEmail.setText(vendedor.getEmail());
                    ftxtCPF.setText(vendedor.getCpf());
                    ftxtCel.setText(vendedor.getCel());
                    ftxtData_Nasc.setText(vendedor.getDataNasc());
                    txtCidade.setText(vendedor.getCidade());
                    cbx_Uf.setSelectedItem(vendedor.getUf());
                    txtRua.setText(vendedor.getRua());
                    txtNumero.setText(vendedor.getNumero());
                    ftxtCEP.setText(vendedor.getCep());
                    txtBairro.setText(vendedor.getBairro());
                    txtComplemento.setText(vendedor.getComplemento());
                    txtSenha.setText(vendedor.getCpf());
                    cbxPermissoes.setSelectedIndex(vendedor.getPermissao());

                    txtBairro.setEnabled(true);
                    txtRua.setEnabled(true);
                    txtCidade.setEnabled(true);
                    txtComplemento.setEnabled(true);
                    txtNumero.setEnabled(true);
                    ftxtCEP.setEnabled(true);
                    cbx_Uf.setEnabled(true);
                    txtSenha.setEnabled(true);
                    txtEmail.setEnabled(true);
                    cbxPermissoes.setEnabled(true);
                    ftxtCel.setEnabled(true);
                    ftxtTel.setEnabled(true);

                    txtEmail.requestFocus();
                    btnAlterar.setEnabled(true);
                    btnDesativarVendedor.setEnabled(true);
                    btnLimpar.setEnabled(true);
                }
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

