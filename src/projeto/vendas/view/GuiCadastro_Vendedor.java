    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        vendedor = null;
        if (txtBusca.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um código!");
            txtBusca.requestFocus();
        } else {
            vendedor = daoVendedor.consultar(parseInt(txtBusca.getText()));

            if (vendedor == null) {
                JOptionPane.showMessageDialog(null, "Código inválido!");
                txtBusca.requestFocus();
            } else {
                vendedor1 = null;
                vendedor1 = daoVendedor.consultarTbVend(parseInt(txtBusca.getText()));
                if (vendedor1 == null) {
                    txtCodigo.setText(String.valueOf(vendedor.getCodigo()));
                    txtNome.setText(vendedor.getNome());
                    ftxtRG.setText(vendedor.getRg());
                    ftxtTel.setText(vendedor.getTel());
                    txtEmail.setText(vendedor.getEmail());
                    ftxtCPF.setText(vendedor.getCpf());
                    ftxtCel.setText(vendedor.getCel());
                    ftxtData_Nasc.setText(vendedor.getDataNasc());

                    txtCidade.setText(vendedor.getCidade());
                    txtUF.setText(vendedor.getUf());
                    txtRua.setText(vendedor.getRua());
                    txtNumero.setText(vendedor.getNumero());
                    ftxtCEP.setText(vendedor.getCep());
                    txtBairro.setText(vendedor.getBairro());
                    txtComplemento.setText(vendedor.getComplemento());
                    txtSenha.setText(vendedor.getCpf());

                    cbxPermissoes.requestFocus();
                    cbxPermissoes.setEnabled(true);
                    txtSenha.setEnabled(true);
                    btnCadastrar.setEnabled(true);
                    btnLimpar.setEnabled(true);
                } else {
                    txtCodigo.setText(String.valueOf(vendedor1.getCodigo()));
                    txtNome.setText(vendedor1.getNome());
                    ftxtRG.setText(vendedor1.getRg());
                    ftxtTel.setText(vendedor1.getTel());
                    txtEmail.setText(vendedor1.getEmail());
                    ftxtCPF.setText(vendedor1.getCpf());
                    ftxtCel.setText(vendedor1.getCel());
                    ftxtData_Nasc.setText(vendedor1.getDataNasc());

                    txtCidade.setText(vendedor1.getCidade());
                    txtUF.setText(vendedor1.getUf());
                    txtRua.setText(vendedor1.getRua());
                    txtNumero.setText(vendedor1.getNumero());
                    ftxtCEP.setText(vendedor1.getCep());
                    txtBairro.setText(vendedor1.getBairro());
                    txtComplemento.setText(vendedor1.getComplemento());
                    txtSenha.setText(vendedor1.getCpf());
                    cbxPermissoes.setSelectedIndex(vendedor1.getPermissao());

                    JOptionPane.showMessageDialog(null, "Este Colaborador já está Cadastrado!");
                    if (vendedor1.getAtivo().equals("I")) {
                        JOptionPane.showMessageDialog(null, "Este Colaborador ja foi cadastrado"
                                + " no passado, você pode reativa-lo se desejar!");
                        btnCadastrar.setText("Reativar Vendedor");
                        btnCadastrar.setEnabled(true);
                    } else {
                        txtBusca.requestFocus();
                        ftxtRG.setText("");
                        ftxtCEP.setText("");
                        ftxtCPF.setText("");
                        ftxtCel.setText("");
                        ftxtData_Nasc.setText("");
                        ftxtTel.setText("");
                        cbxPermissoes.setSelectedIndex(0);

                        txtRua.setText("");
                        txtNumero.setText("");
                        txtBairro.setText("");
                        txtCidade.setText("");
                        txtUF.setText("");
                        txtComplemento.setText("");

                        txtSenha.setText("");
                        txtNome.setText("");
                        txtCodigo.setText("");
                        txtEmail.setText("");
                        cbxPermissoes.setEnabled(false);
                        txtBusca.setText("");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

